export function getAllArticles(): Article[] {
  return [...articles].sort((a, b) => (a.date < b.date ? 1 : -1));
}

export function getLatestArticles(n: number): Article[] {
  return getAllArticles().slice(0, n);
}

export function getArticle(slug: string): Article | undefined {
  return articles.find((a) => a.slug === slug);
}

/** Дата последнего изменения статьи (updated, если задана, иначе date). */
export function articleLastModified(a: Article): string {
  return a.updated ?? a.date;
}
